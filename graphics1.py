#PENTAGON using turtle graphics
import turtle
turtle.fillcolor("violet")  #colours the turtle

turtle.pencolor("red")
turtle.forward(100)
turtle.left(90)
turtle.pencolor("brown")
turtle.forward(100)
turtle.left(60)
turtle.pencolor("blue")
turtle.forward(100)
turtle.left(60)
turtle.pencolor("red")
turtle.forward(100)
turtle.left(60)
turtle.pencolor("brown")
turtle.forward(100)
turtle.left(90)
turtle.pencolor("blue")
turtle.forward(100)

turtle.hideturtle()
turtle.update()
turtle.done()
turtle.exitonclick()
